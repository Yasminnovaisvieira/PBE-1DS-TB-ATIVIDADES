# 11 - Calculando o de base elevado a expoente com números solicitados ao usuário.

print("O programa irá calcular a base elevado a expoente.")

base = int(input("Digite o valor da base:"))
expoente = int(input("Digite o valor do expoente:"))

print(f"O valor de {base} elevado a {expoente} é: {base ** expoente}.")